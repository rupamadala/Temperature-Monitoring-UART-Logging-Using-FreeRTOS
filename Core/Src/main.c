void DHT11_task(void *argument){
	DHT_D11_Data sensor_data;
	uint32_t tick = osKernelGetTickCount();

	for(;;)
	{
		Read_DHT11(&sensor_data);  // Your function fills the struct

		// Send data to logging task
		xQueueSend(DHT11_sensor_handle, &sensor_data, 0);

		tick += 2000;              // Next wakeup after 2 sec
		osDelayUntil(tick);
	}

}

void Log_task(void *argument){
	DHT_D11_Data sensor_data;
	char response[100];
	if(xQueueReceive(DHT11_sensor_handle, &sensor_data,portMAX_DELAY) == 1){
		sprintf(response,"Temperature: %d.%d",sensor_data.temp_data,sensor_data.temp_dec_data);
		HAL_UART_Transmit(&hlpuart1, (uint8_t *) response, strlen(response), HAL_MAX_DELAY);
	}

}

